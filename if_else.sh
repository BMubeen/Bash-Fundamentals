#!/bin/bash
# What is it?
# An if/else statement allows your program to make decisions.
# It runs different code depending on whether a condition is true or false.
# How it works:
# if [ condition ]
# then
#   code runs if condition is true
# else
#   code runs if condition is false
# fi
# Rules:
# - Always use spaces inside [ ]
# - Must include then and fi
# - Conditions use operators like -gt, -lt, -eq
# Common mistakes:
# missing spaces: if[$num -gt 5]
# forgetting then or fi
# wrong operators
# Troubleshooting:
# Check spacing inside brackets
# Make sure then and fi are included
# Print variables using echo to debug